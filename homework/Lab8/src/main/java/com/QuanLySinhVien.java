    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
    try {
        if (!txtMaSV.getText().equals("")) {
            SinhvienDAO dao = new SinhvienDAO();
            SinhVien sv = dao.findByMaSV(txtMaSV.getText());

            if (sv != null) {
                // Populate the fields with the found student's data
                txtMaSV.setText(sv.getMaSV());
                txtHolot.setText(sv.getHolot());
                txtTen.setText(sv.getTen());
                txtMaLop.setText(sv.getMalop());
                txtTenLop.setText(sv.getTenlop());
                txtSoDT.setText(sv.getSoDT());
                txtEmail.setText(sv.getEmail());

                // Enable update and delete buttons
                btnCapNhat.setEnabled(true);
                btnXoa.setEnabled(true);
            } else {
                // Show message if student is not found
                JOptionPane.showMessageDialog(
                    this, 
                    "Sinh viên " + txtMaSV.getText() + " không có trong danh sách",
                    "Thông báo lỗi", 
                    JOptionPane.INFORMATION_MESSAGE
                );

                // Clear fields
                txtHolot.setText("");
                txtTen.setText("");
                txtMaLop.setText("");
                txtTenLop.setText("");
                txtSoDT.setText("");
                txtEmail.setText("");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Cho biết mã Sinh viên để tìm kiếm");
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
    try {
        if (!txtMaSV.getText().equals("")) {
            SinhvienDAO dao = new SinhvienDAO();
            
            if (dao.delete(txtMaSV.getText())) {
                // Show success message and update the table
                JOptionPane.showMessageDialog(this, "Xóa thành công 1 sinh viên");
                fillTable();
                resetForm();

                // Disable update and delete buttons
                btnCapNhat.setEnabled(false);
                btnXoa.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(this, "Không thể xóa sinh viên này");
            }
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnXoaActionPerformed

