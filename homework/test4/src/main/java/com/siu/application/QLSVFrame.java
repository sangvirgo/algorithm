    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        try {
            SinhVien temp=new SinhVien();
            temp.setMaSV(txtMaSv.getText());
            temp.setHoLot(txtHoLot.getText());
            temp.setTen(txtTen.getText());
            temp.setMaLop(txtMaLop.getText());
            temp.setTenLop(txtTenLop.getText());
            temp.setSoDT(txtSoDT.getText());
            temp.setEmail(txtEmail.getText());
            if((new SinhVienDAOImpl().update(temp))) {
                JOptionPane.showMessageDialog(this, "Cap nhat thanh cong");
                reset();
                fillTable();
                btnCapNhat.setEnabled(false);
                btnXoa.setEnabled(true);
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        try {
            if(new SinhVienDAOImpl().insert(sv)) {
                sv=null;
                JOptionPane.showMessageDialog(this, "Da them mot sinh vien", "Them thanh cong", JOptionPane.INFORMATION_MESSAGE);
                btnLuu.setEnabled(false);
                fillTable();
            } else {
                JOptionPane.showMessageDialog(this, "Them that bai");
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        try {
            SinhVien temp=new SinhVien();
            String masv=txtMaSv.getText();
            temp=new SinhVienDAOImpl().findById(masv);
            if(!masv.equals("")) {
                if(temp!=null) {
                    txtEmail.setText(temp.getEmail());
                    txtHoLot.setText(temp.getHoLot());
                    txtTenLop.setText(temp.getTenLop());
                    txtMaLop.setText(temp.getMaLop());
                    txtSoDT.setText(temp.getSoDT());
                    txtTen.setText(temp.getTen());
                } else {
                    JOptionPane.showMessageDialog(this, "Khong tim thay "+ masv+ " trong danh sach", "thong bao loi", JOptionPane.INFORMATION_MESSAGE);
                    reset();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Nhap ma sinh vien de tim kiem");
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void tbSinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSinhVienMouseClicked
        // TODO add your handling code here:ta
        int row=tbSinhVien.getSelectedRow();
        if(row >=0) {
            txtEmail.setText(tbSinhVien.getValueAt(row, 6).toString());
            txtHoLot.setText(tbSinhVien.getValueAt(row, 1).toString());
            txtTenLop.setText(tbSinhVien.getValueAt(row, 4).toString());
            txtMaLop.setText(tbSinhVien.getValueAt(row, 3).toString());
            txtMaSv.setText(tbSinhVien.getValueAt(row, 0).toString());
            txtSoDT.setText(tbSinhVien.getValueAt(row, 5).toString());
            txtTen.setText(tbSinhVien.getValueAt(row, 2).toString());
            btnXoa.setEnabled(true);
            btnCapNhat.setEnabled(true);
        }
    }//GEN-LAST:event_tbSinhVienMouseClicked

