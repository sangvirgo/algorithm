    public void initTable() {
        String[] header=new String[]{"Ma SV", "Ho lot", "Ten", "Ma lop", "Ten lop", "So DT", "Email"};
        model.setColumnIdentifiers(header);
        tbSinhVien.setModel(model);
    }
    
    private void fillTable() {
        try {
            List<SinhVien> result=new SinhVienDAOImpl().getAll();
            model.setRowCount(0);
            for(SinhVien sv: result) {
                model.addRow(new Object[]{
                    sv.getMaSV(),
                    sv.getHoLot(),
                    sv.getTen(),
                    sv.getMaLop(),
                    sv.getTenLop(),
                    sv.getSoDT(),
                    sv.getEmail()
                });
            }
            model.fireTableDataChanged();
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
