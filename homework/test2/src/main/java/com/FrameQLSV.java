    private void fillTable() {
        try {
            List<SinhVien> listStudent=SinhVienDAO.getInstance().getAll();
            model.setRowCount(0);
            for (SinhVien sv : listStudent) {
                model.addRow(new Object[]{
                    sv.getMaSV(), sv.getHoLot(), sv.getTen(), sv.getMaLop(),
                    sv.getTenLop(), sv.getSoDT(), sv.getEmail()
            });
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Loi khi load"+e.getMessage());
            e.printStackTrace();
        }
    }
    
