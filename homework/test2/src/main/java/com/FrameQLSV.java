    private void tbSVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSVMouseClicked
        // TODO add your handling code here:
        int row = tbSV.getSelectedRow();
        if (row>=0) {
            txtMaSV.setText(tbSV.getValueAt(row, 0).toString());
            txtHoLot.setText(tbSV.getValueAt(row, 1).toString());
            txtTen.setText(tbSV.getValueAt(row, 2).toString());
            txtMaLop.setText(tbSV.getValueAt(row, 3).toString());
            txtTenLop.setText(tbSV.getValueAt(row, 4).toString());
            txtSDT.setText(tbSV.getValueAt(row, 5).toString());
            txtEmail.setText(tbSV.getValueAt(row, 6).toString()); 
            
            btnCapNhat.setEnabled(true);
            btnXoa.setEnabled(true);
        }
    }//GEN-LAST:event_tbSVMouseClicked

